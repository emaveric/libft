#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*s;

	i = 0;
	while (str[i] != '\0')
		i++;
	s = (char *)malloc(sizeof(char) * (i + 1));
	if (s == NULL)
		return (0);
	i = 0;
	while (str[i] != '\0')
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
