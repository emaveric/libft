#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t count)
{
	size_t i;

	i = 0;
	while (s2[i] != '\0' && i < count)
	{
		s1[i] = s2[i];
		i++;
	}
	while (i < count)
	{
		s1[i] = '\0';
		i++;
	}
	return (s1);
}
